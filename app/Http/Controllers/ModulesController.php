    {
        dd($course);
        $module = new Module($request->all());
        $module->course()->associate($course);
        $module->user()->associate(auth()->user());

        if($module->save()) {
            return redirect()->route('courses.show', $course);
        } else {
            return view('modules.create', ['module' => $module]);
        }
